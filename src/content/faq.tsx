/**
 * FAQ (よくある質問) データ定義。
 *
 * `/faq` ページから読み込まれる。
 * コンテンツ系 (ラーメン知識) 12 件 + サービス系 (アプリの説明) 8 件 = 全 20 件。
 *
 * 設計上の狙い:
 * - 検索エンジン / AI 検索での引用を得やすい「Q&A 形式」でラーメン知識を提供する (SEO 強化第二弾)。
 * - Schema.org FAQPage スキーマにそのまま流し込めるよう、`answer` はプレーンテキストで保持する。
 *   マークアップは表示側 (Faq ページ) で行い、構造化データには `answer` の生テキストを使う。
 *
 * データ追加時のルール:
 * - `id` はユニークかつ URL アンカー (`#q-xxxx`) に使うので kebab-case で英字のみ。
 * - `category`: `'content'` (ラーメン知識) / `'service'` (アプリ運用) のいずれか。
 * - `question` / `answer` はプレーンテキスト。改行は含めない (Schema.org 上で扱いづらいため)。
 */

/** FAQ 項目のカテゴリ。表示時の見出し分けにも使う。 */
export type FaqCategory = 'content' | 'service';

/** カテゴリ表示用ラベル (見出しに使用)。 */
export const FAQ_CATEGORY_LABELS: Record<FaqCategory, string> = {
  content: 'ラーメン知識について',
  service: 'このサービスについて',
};

/** FAQ 1 件の定義。 */
export interface FaqItem {
  /** アンカー用 ID (kebab-case, 英字のみ推奨)。 */
  id: string;
  /** カテゴリ (content: ラーメン知識 / service: アプリ運用)。 */
  category: FaqCategory;
  /** 質問文 (プレーンテキスト)。 */
  question: string;
  /** 回答文 (プレーンテキスト, Schema.org FAQPage の acceptedAnswer.text に直接投入する)。 */
  answer: string;
}

