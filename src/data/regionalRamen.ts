/**
 * slug から都道府県データを取得する。存在しない場合は undefined。
 */
export function findRegionalRamenBySlug(slug: string): RegionalRamen | undefined {
  return REGIONAL_RAMEN.find((r) => r.prefectureSlug === slug);
}

/**
 * 地方ごとにグルーピングした都道府県一覧を返す。
 * 一覧ページの表示で使う。
 */
export function groupRegionalRamenByRegion(): Record<Region, ReadonlyArray<RegionalRamen>> {
  const initial: Record<Region, RegionalRamen[]> = {
    北海道: [],
    東北: [],
    関東: [],
    中部: [],
    近畿: [],
    中国: [],
    四国: [],
    九州: [],
  };
  for (const item of REGIONAL_RAMEN) {
    initial[item.region].push(item);
  }
  return initial;
}
