/** 都道府県内で紹介する 1 種類のラーメン。 */
export interface RamenType {
  /** 系統・スタイル名 (例: '札幌ラーメン', '博多ラーメン')。 */
  name: string;
  /** 特徴を 1〜3 文で。 */
  description: string;
  /** スープの特徴 (例: '味噌ベースの濃厚スープ')。 */
  soup: string;
  /** 麺の特徴 (例: '中太の縮れ麺')。 */
  noodle: string;
  /** 主なトッピング (例: 'チャーシュー、メンマ、モヤシ、コーン、バター')。 */
  toppings: string;
  /** 発祥店・時期 (任意, 事実性が不確かなら「諸説あり」等を明記)。 */
  origin?: string;
  /** 主な提供エリア (任意)。 */
  area?: string;
}

/** 1 つの都道府県のご当地ラーメンデータ。 */
export interface RegionalRamen {
  /** 都道府県名 (例: '北海道', '青森県')。 */
  prefecture: string;
  /** URL slug (例: 'hokkaido', 'aomori')。英字のみ、`/regions/:prefectureSlug` に使う。 */
  prefectureSlug: string;
  /** 所属地方。 */
  region: Region;
  /** その県のご当地ラーメン一覧 (代表系統から順に並べる)。 */
  ramenTypes: ReadonlyArray<RamenType>;
  /** その土地のラーメン文化 (朝ラー等) の説明 (任意)。 */
  culture?: string;
  /** 関連する用語 (glossary の `id` を参照)。詳細ページで用語辞典へのリンクを作る。 */
  relatedTerms?: ReadonlyArray<string>;
}

